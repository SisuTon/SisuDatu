pip install -r requirements.txt

# –ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é
log_info "–ü—Ä–æ–≤–µ—Ä—è–µ–º –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏—é..."
python check_config.py

if [ $? -ne 0 ]; then
    log_error "–ü—Ä–æ–±–ª–µ–º—ã —Å –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–µ–π!"
    log_info "–°–æ–∑–¥–∞–µ–º —à–∞–±–ª–æ–Ω .env —Ñ–∞–π–ª–∞..."
    python check_config.py --create-template
    log_warning "–ó–∞–ø–æ–ª–Ω–∏—Ç–µ .env —Ñ–∞–π–ª –∏ –∑–∞–ø—É—Å—Ç–∏—Ç–µ —Å–∫—Ä–∏–ø—Ç —Å–Ω–æ–≤–∞!"
    exit 1
fi

# –ó–∞–ø—É—Å–∫–∞–µ–º —Ç–µ—Å—Ç—ã —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª–∞
log_info "–ó–∞–ø—É—Å–∫–∞–µ–º —Ç–µ—Å—Ç—ã —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª–∞..."
python test_bot_functionality.py

if [ $? -ne 0 ]; then
    log_error "–¢–µ—Å—Ç—ã –Ω–µ –ø—Ä–æ—à–ª–∏!"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–∏–Ω—Ç–∞–∫—Å–∏—Å Python —Ñ–∞–π–ª–æ–≤
