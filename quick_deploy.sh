log_info "–î–ª—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞ –∏—Å–ø–æ–ª—å–∑—É–π—Ç–µ: tail -f bot.log"
log_info "–î–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏: kill $BOT_PID"

# –°–æ–∑–¥–∞–µ–º —Å–∫—Ä–∏–ø—Ç –¥–ª—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏
cat > stop_bot.sh << EOF
