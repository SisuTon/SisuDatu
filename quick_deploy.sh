    log_success "–ë–æ—Ç —É—Å–ø–µ—à–Ω–æ –∑–∞–ø—É—â–µ–Ω! PID: $BOT_PID"
    log_info "–õ–æ–≥–∏ –±–æ—Ç–∞: tail -f bot.log"
    log_info "–û—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –±–æ—Ç–∞: kill $BOT_PID"
    
    # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–µ –ª–æ–≥–∏
    log_info "–ü–æ—Å–ª–µ–¥–Ω–∏–µ –ª–æ–≥–∏ –±–æ—Ç–∞:"
    tail -n 20 bot.log
    
